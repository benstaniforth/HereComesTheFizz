package benstaniforth.HereComesTheFizz;

public class Words {

    boolean Fizz = false;
    boolean Buzz = false;
    boolean Bang = false;
    boolean Bong = false;
    boolean Fezz = false;
    boolean Reverse = false;

    public Words(boolean fizz, boolean buzz, boolean bang, boolean bong, boolean fezz, boolean reverse) {
        Fizz = fizz;
        Buzz = buzz;
        Bang = bang;
        Bong = bong;
        Fezz = fezz;
        Reverse = reverse;
    }
}
